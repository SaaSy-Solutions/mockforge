// Export all predefined themes
export const predefinedThemes: ThemePalette[] = [
  coreBrandTheme,
  professionalBlueTheme,
  forestGreenTheme,
  twilightPurpleTheme,
  oceanDeepTheme,
];

// Utilities
export function getThemeById(id: string): ThemePalette | undefined {
  return predefinedThemes.find(theme => theme.id === id);
}

export function getDefaultTheme(): ThemePalette {
  return coreBrandTheme;
}

export function createCustomTheme(
  name: string,
  description: string,
  lightColors: Record<string, string>,
  darkColors: Record<string, string>
): ThemePalette {
  return {
    id: `custom-${Date.now()}`,
    name,
    description,
    preview: {
      primary: lightColors['--brand'] || '#000000',
      secondary: lightColors['--bg-secondary'] || '#FFFFFF',
      accent: lightColors['--success'] || '#10B981',
    },
    colors: {
      light: lightColors,
      dark: darkColors,
    },
  };
}
